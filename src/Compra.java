    private void ListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListaActionPerformed
        String clave = jTextField1.getText();
        String proveedor = jTextField2.getText();
        String tipo = jTextField3.getText();
        String producto = jTextField4.getText();
        int piezas = Integer.parseInt(jTextField5.getText());
        int precio = Integer.parseInt(jTextField6.getText());
        Object Datos[] = {clave,proveedor,tipo,producto,piezas, precio};
        mo.addRow(Datos);
        p = new Producto(clave,proveedor,tipo,producto,piezas, precio);
        ArrPro.agregar(p);
        limpiar();
    }//GEN-LAST:event_ListaActionPerformed

